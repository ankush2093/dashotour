// toggle menu

let link = document.querySelectorAll(".navAnchor");

link.forEach(btn => {
    btn.addEventListener('click', () => {

        link.forEach(other => {
            if (other !== btn) {
                other.parentElement.classList.remove("active");
            }
        })

        btn.parentElement.classList.toggle("active");

    })
})


